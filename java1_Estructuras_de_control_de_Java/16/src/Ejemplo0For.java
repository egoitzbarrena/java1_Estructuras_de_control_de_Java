/*
 * programa que muestra los números del 1 al 10
 */
//definición de la clase Ejemplo0For
public class Ejemplo0For {
//define el método main
    public static void main(String[] args) {
//define la variable de tipo integer
        int i;
//valor inicial de la variable 1, durante el valor de i sea menor o igual a 10 escribe en la pantalla el valor de i incrementando 1.
        for(i=1; i<=10;i++)
            System.out.println(i + " ");
    }
}
