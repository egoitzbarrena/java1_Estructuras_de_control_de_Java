/*
 * Programa que lee un número entre 1 y 10 ambos incluidos
 */
//importa todas las clases  del paquete java.util
import java.util.*;
//definición de la clase Ejemplo2DoWhile
public class Ejemplo2DoWhile {
//define el método main
    public static void main(String[] args) {
//define la variable de tipo integer
        int n;
//utiliza la clase scanner para recojer todo lo que el usuario meta por el teclado y lo guarda en la variable sc de tipo Scanner.
        Scanner sc = new Scanner( System.in );

        do {
//pinta en la pantalla un texto. 
            System.out.print("Escribe un número entre 1 y 10: ");
//recoje lo que has introducido en la variable n.
            n = sc.nextInt();
        }
//mientras el numero sea menor a 1 o mayor a 10 va a ejecutar las siguientes lineas.
while (n<1 || n >10);
//pinta en la pantalla un texto. la frase has introducido y el valor de la variable n.
        System.out.println("Ha introducido: " + n);
    }
}
